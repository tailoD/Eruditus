package com.eruditus.models;

public class Aluno {
    private String nome;
    private Integer id;
    private String sexo;
    private Integer idade;
    
    public Aluno(String nome, Integer id, String sexo, Integer idade){
        this.nome = nome;
        this.id = id;
        this.sexo = sexo;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public Integer getIdade() {
        return idade;
    }

    public void setIdade(Integer idade) {
        this.idade = idade;
    }
    
    
}